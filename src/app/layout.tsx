import type { Metadata } from "next";
import {Exo_2 } from "next/font/google";
import "./globals.css";
import { Newsreader, Fraunces } from "next/font/google";
import {
    NavigationMenu,
    NavigationMenuItem,
    NavigationMenuLink,
    NavigationMenuList,
} from "@/components/ui/navigation-menu"
import Link from "next/link";
import { createClient } from "@/lib/supabase";
import { Avatar, AvatarImage, AvatarFallback } from "@/components/ui/avatar";
import { Button } from "@/components/ui/button";

const exo_2 = Exo_2({
    variable: "--font-exo-2",
    subsets: ["latin"],
    weight: ["400", "700"]
});

const newsReader = Newsreader({
    variable: "--font-newsreader",
    subsets: ["latin"]
})

const fraunces = Fraunces({
    variable: "--font-fraunces",
    subsets: ["latin"]
})

export const metadata: Metadata = {
    title: "Vega",
    description: "Generated by create next app",
};

export default async function RootLayout({
    children,
}: Readonly<{
    children: React.ReactNode;
}>) {
    const supabase = await createClient();
    const { data: { user } } = await supabase.auth.getUser();
    return (
        <html className={exo_2.className} lang="en">
            <body className="dark">
                <nav
                    className="grid grid-cols-3 items-center p-4 sticky top-0 z-50"
                    style={{ background: "var(--background)", borderBottom: "1px solid #E0E2DB" }}
                >
                    <div className="text-4xl font-bold pl-2" style={{ color: "#E0E2DB" }}>VEGA</div>
                    <div className="flex justify-center">
                        <NavigationMenu>
                            <NavigationMenuList className="flex space-x-8">
                                <Link href={"/"}>
                                    <NavigationMenuItem>
                                        <NavigationMenuLink className="text-2xl transition-all duration-50 hover:text-purple-400 hover:scale-110 underline-offset-4">Home</NavigationMenuLink>
                                    </NavigationMenuItem>
                                </Link>
                                <Link href={"/about"}>
                                    <NavigationMenuItem>
                                        <NavigationMenuLink className="text-2xl transition-all duration-50 hover:text-purple-400 hover:scale-110 underline-offset-4">About</NavigationMenuLink>
                                    </NavigationMenuItem>
                                </Link>
                                <Link href={"/pricing"}>
                                    <NavigationMenuItem>
                                        <NavigationMenuLink className="text-2xl transition-all duration-50 hover:text-purple-400 hover:scale-110 underline-offset-4">Pricing</NavigationMenuLink>
                                    </NavigationMenuItem>
                                </Link>
                            </NavigationMenuList>
                        </NavigationMenu>
                    </div>
                   
                    <div className="ml-auto flex items-center gap-3">
                        {user ? (
                            <Link href="/protected">
                                <Avatar className="border-2 p-5 border-purple-400 hover:border-purple-300 transition-colors">
                                    <AvatarImage src={user.user_metadata?.avatar_url ?? undefined} alt={user.email ?? "User"} />
                                    <AvatarFallback>{user.email?.[0]?.toUpperCase() ?? "U"}</AvatarFallback>
                                </Avatar>
                            </Link>
                        ) : (
                            <Button asChild size="sm">
                                <Link href="/login">Log in</Link>
                            </Button>
                        )}
                    </div>
                </nav>
                {children}
            </body>
        </html>
    );
}
