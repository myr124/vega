import type { Metadata } from "next";
import { Geist, Geist_Mono } from "next/font/google";
import "./globals.css";
import { Newsreader, Fraunces } from "next/font/google";
import {
    NavigationMenu,
    NavigationMenuContent,
    NavigationMenuIndicator,
    NavigationMenuItem,
    NavigationMenuLink,
    NavigationMenuList,
    NavigationMenuTrigger,
    NavigationMenuViewport,
} from "@/components/ui/navigation-menu"
import Link from "next/link";
import { createClient } from "@/lib/supabase";
import { Avatar, AvatarImage, AvatarFallback } from "@/components/ui/avatar";
import { Button } from "@/components/ui/button";

const geistSans = Geist({
    variable: "--font-geist-sans",
    subsets: ["latin"],
});

const geistMono = Geist_Mono({
    variable: "--font-geist-mono",
    subsets: ["latin"],
});

const newsReader = Newsreader({
    variable: "--font-newsreader",
    subsets: ["latin"]
})

const fraunces = Fraunces({
    variable: "--font-fraunces",
    subsets: ["latin"]
})

export const metadata: Metadata = {
    title: "Vega",
    description: "Generated by create next app",
};

export default async function RootLayout({
    children,
}: Readonly<{
    children: React.ReactNode;
}>) {
    const supabase = await createClient();
    const { data: { user } } = await supabase.auth.getUser();
    return (
        <html lang="en" className={`${newsReader.className}`}>
            <body className="dark antialiased">
                <nav className="flex items-center p-4 bg-transparent border-b border-border/50">
                    <div className="text-2xl font-bold text-foreground mr-8">VEGA</div>
                    <div className="flex-1 flex justify-center">
                        <NavigationMenu className="flex">
                            <NavigationMenuList className="flex space-x-8">
                                <Link href="/">
                                    <NavigationMenuItem>
                                        <NavigationMenuLink className="text-foreground hover:text-primary">Home</NavigationMenuLink>
                                    </NavigationMenuItem>
                                </Link>
                                <Link href="/about">
                                    <NavigationMenuItem>
                                        <NavigationMenuLink className="text-foreground hover:text-primary">About</NavigationMenuLink>
                                    </NavigationMenuItem>
                                </Link>
                                <Link href="/pricing">
                                    <NavigationMenuItem>
                                        <NavigationMenuLink className="text-foreground hover:text-primary">Pricing</NavigationMenuLink>
                                    </NavigationMenuItem>
                                </Link>
                            </NavigationMenuList>
                        </NavigationMenu>
                    </div>
                    <div className="ml-auto flex items-center gap-3">
                        {user ? (
                            <Link href="/protected">
                                <Avatar>
                                    <AvatarImage src={user.user_metadata?.avatar_url ?? undefined} alt={user.email ?? "User"} />
                                    <AvatarFallback>{user.email?.[0]?.toUpperCase() ?? "U"}</AvatarFallback>
                                </Avatar>
                            </Link>
                        ) : (
                            <Button asChild size="sm">
                                <Link href="/login">Log in</Link>
                            </Button>
                        )}
                    </div>
                </nav>
                {children}
            </body>
        </html>
    );
}
